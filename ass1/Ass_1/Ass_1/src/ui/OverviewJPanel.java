    public void displayTable(List<EmployeeInfo> emp){
        DefaultTableModel model = (DefaultTableModel) tblOverview.getModel();
        model.setRowCount(0);
        for(EmployeeInfo employee : history.getHistory()){
            Object[] row = new Object[11];
            row[0] = employee.getName();
            row[1] = employee.getEmployeeID();
            row[2] = employee.getAge();
            row[3] = employee.getGender();
            row[4] = employee.getStartDate();
            row[5] = employee.getLevel();
            row[6] = employee.getTeaminfo();
            row[7] = employee.getPositionTitle();
            row[8] = employee.getCellNum();
            row[9] = employee.getEmailAdd();
//            row[10] = employee.getPhoto();
            model.addRow(row);
        }
    }
