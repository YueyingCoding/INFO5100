    private void btnView2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnView2ActionPerformed
        // TODO add your handling code here:
        ViewDetailJPanel.setVisible(true);
        OverviewJPanel.setVisible(false);
        
        int selectedRowIndex = tblOverview.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select A Row To View.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)tblOverview.getModel();
        EmployeeInfo selectedEmployee = (EmployeeInfo)model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(selectedEmployee.getName());
        txtAge.setText(String.valueOf(selectedEmployee.getAge()));
        txtCellNum.setText(selectedEmployee.getCellNum());
        cmbGender.setSelectedItem(selectedEmployee.getGender());
        txtEmailAdd.setText(selectedEmployee.getEmailAdd());
        txtEmployeeID.setText(String.valueOf(selectedEmployee.getEmployeeID()));
        txtStartDate.setValue(selectedEmployee.getStartDate());
        txtLevel.setText(String.valueOf(selectedEmployee.getLevel()));
        txtTeamInfo.setText(selectedEmployee.getTeaminfo());
        txtPosTitle.setText(selectedEmployee.getPositionTitle());
        lblPicture.setIcon(selectedEmployee.getImage());
        
    }//GEN-LAST:event_btnView2ActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblOverview.getSelectedRow();
        
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select A Row To Delete.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)tblOverview.getModel();
        EmployeeInfo selectedEmployee = (EmployeeInfo)model.getValueAt(selectedRowIndex, 0);
        
        history.deleteEmployee(selectedEmployee);
        
        String Name = txtName.getText();
        int Age = Integer.parseInt(txtAge.getText());
        int EmployeeID = Integer.parseInt(txtEmployeeID.getText());
        String Gender = cmbGender.getSelectedItem().toString();
        Date StartDate = (Date)txtStartDate.getValue();
        int Level = Integer.parseInt(txtLevel.getText());
        String Teaminfo = txtTeamInfo.getText();
        String PositionTitle = txtPosTitle.getText();
        String CellNum = txtCellNum.getText();
        String EmailAdd = txtEmailAdd.getText();
        ImageIcon image = (ImageIcon)lblPicture.getIcon();
        
        EmployeeInfo ei = history.addNewEmployees();
        ei.setAge(Age);
        ei.setCellNum(CellNum);
        ei.setEmailAdd(EmailAdd);
        ei.setEmployeeID(EmployeeID);
        ei.setGender(Gender);
        ei.setLevel(Level);
        ei.setName(Name);
        ei.setStartDate(StartDate);
        ei.setTeaminfo(Teaminfo);
        ei.setPositionTitle(PositionTitle);
        ei.setImage(image);
        
        JOptionPane.showMessageDialog(this, "Employee Info Updated.");
    }//GEN-LAST:event_btnUpdateActionPerformed

